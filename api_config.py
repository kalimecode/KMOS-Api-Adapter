#!/usr/bin/env python3
"""
=============================================================================
КОНФИГУРАЦИЯ API-АДАПТЕРА
=============================================================================
Этот файл содержит все настраиваемые параметры для API-адаптера.
Редактируйте только этот файл для изменения настроек.
После изменений — перезапустите службу: sudo systemctl restart api-adapter

Формат даты и времени во всём проекте: ДД.ММ.ГГГГ ЧЧ:ММ:СС
=============================================================================
"""

# =============================================================================
# НАСТРОЙКИ СЕРВЕРА
# =============================================================================

# IP-адрес для прослушивания API
# "0.0.0.0" — все интерфейсы (доступно из сети)
# "127.0.0.1" — только локально (без доступа из сети)
API_HOST = "0.0.0.0"

# Порт, на котором будет работать API
# Убедитесь, что порт не занят другими службами
API_PORT = 81

# =============================================================================
# 🏭 БЛОК БГЕ (MODBUS)
# =============================================================================

# IP-адрес контроллера БГЕ в локальной сети
IP_BGE = "192.168.1.2"

# TCP-порт Modbus (стандартный: 502)
PORT_BGE = 502

# Адрес устройства (slave ID) в сети Modbus
# Обычно 1, если устройство одно
UNIT_ID_BGE = 1

# Таймаут ожидания ответа от контроллера (в секундах)
# Увеличьте, если сеть медленная или устройство отвечает долго
MODBUS_TIMEOUT = 2.0

# Количество повторных попыток при ошибке чтения
# При каждой неудаче делается пауза перед следующей попыткой
MODBUS_RETRIES = 3


# =============================================================================
# 📷 КАМЕРА: ЗАХВАТ И ОТДАЧА ЧЕРЕЗ API
# =============================================================================

# Полный путь к файлу последнего снимка камеры
# Сюда будет сохранять camera_capture.py
CAM_IMAGE = "/etc/zabbix/scripts/camera/lastest.png"

# Папка для раздачи статических файлов через API
# API будет отдавать файлы из этой папки по запросу /camera/имя_файла
CAMERA_STATIC_DIR = "/etc/zabbix/scripts/camera"

# URL-префикс для доступа к камере через API
# Пример: http://<сервер>:81/camera/latest.png
CAMERA_URL_PREFIX = "/camera"

# Базовый URL для ссылки в JSON-ответах
# Используется в /api/camera для формирования ссылки на изображение
CAM_URL_BASE = f"http://localhost:{API_PORT}{CAMERA_URL_PREFIX}"

# Максимально допустимый возраст снимка в секундах
# Если файл не обновляется дольше — статус камеры = 0 (неисправна)
CAM_MAX_AGE_SECONDS = 60


# =============================================================================
# ТЕМПЕРАТУРНЫЕ ДАТЧИКИ
# =============================================================================

# Словарь датчиков: "имя_в_JSON": "путь_к_устройству"
# Можно добавить неограниченное количество датчиков
# Раскомментируйте строки для подключения новых устройств
TEMP_SENSORS = {
    "Температура в шкафу": "/dev/hidraw0",
    # "Датчик 2 (наружний"": "/dev/hidraw1",        # ← Второй датчик (когда подключите)
    # "Датчик 3": "/dev/hidraw2",                   # ← Третий датчик (когда подключите)
}

# =============================================================================
# ЛОГИРОВАНИЕ
# =============================================================================

# Путь к файлу логов API-адаптера
# Файл создаётся автоматически при первом запуске
LOG_FILE = "/etc/zabbix/scripts/api_log.log"

# Максимальный размер одного файла лога в байтах
# 10 MB = 10 × 1024 × 1024 = 10485760 байт
LOG_MAX_BYTES = 10 * 1024 * 1024  # 10 MB

# Количество файлов лога для хранения (ротация)
# Старые файлы удаляются при превышении этого числа
LOG_BACKUP_COUNT = 10

# Уровень логирования
# "DEBUG" — подробные логи (все запросы, детали)
# "INFO"  — стандартные логи (запросы, ошибки)
# "WARNING" — только предупреждения и ошибки
# "ERROR" — только критические ошибки
LOG_LEVEL = "INFO"

# =============================================================================
# ФОРМАТЫ ДАННЫХ
# =============================================================================

# Отступы в JSON-ответах (для читаемости)
# 0 — компактный формат (без отступов)
# 2 — стандартные отступы (рекомендуется)
# 4 — большие отступы (для отладки)
JSON_INDENT = 2

# Кодировка для JSON-ответов
# Не рекомендуется менять (utf-8 поддерживает кириллицу)
JSON_ENCODING = "utf-8"

# =============================================================================
# СИСТЕМНЫЕ ПАРАМЕТРЫ (НЕ МЕНЯТЬ БЕЗ НЕОБХОДИМОСТИ)
# =============================================================================

# Интервал между проверками доступности устройств (в секундах)
HEALTH_CHECK_INTERVAL = 60

# Таймаут для проверки доступности Modbus-устройства (в секундах)
MODBUS_HEALTH_TIMEOUT = 0.5
